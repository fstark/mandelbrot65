ECHO = $FFEF

PTR = $00

PRINTALL:
	LDA #$0d
	JSR ECHO
	JSR ECHO
	JSR ECHO
	LDA #<DATA
	STA PTR
	LDA #>DATA
	STA PTR+1
	LDY #0
LOOP:
	LDA (PTR),Y
	BEQ DONE
	CMP #$FF
	BEQ PRINTALL
	JSR ECHO
	JSR INCPTR
	JMP LOOP
DONE:
